##### Integer #####
a=10  #int
print(a) #10
print(type(a))  # <class 'int'>

##### String #####
c="20.05"  #string
print(c)  #20.05
print(type(c)) #<class 'str'>

######## Conversion of an integer to Float ########
b=float(a)
print("int to float",b)  #int to float 10.0
print(type(b))  #<class 'float'>

######### Conversion of an string to Float #########
d=float(c) #float
print(type(d))  #<class 'float'>
print("string to float",d)   #string to float 20.05
